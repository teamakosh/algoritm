# Yaxshi algoritmning belgilari:
# Kirish (muammo) va chiqish (yechim) aniq berilgan bo'lishi kerak.
# Algortimning har bir bosqichi aniq va ravshan bo'lishi kerak.
# Algoritm muammoni hal qilishning turli usullari orasida eng samaralisi bo'lishi kerak.
# Algoritmda kompyuter kodi bo'lmasligi kerak. Buning o'rniga algoritm turli xil dasturlash tillarida ishlatilishi mumkin bo'lgn tarzda yozilishi kerak.

# Foydalanuvchi kiritgan ikki sonni qo'shish algoritmi: 
# 1. Start
# 2. son1, son2 va summa o'zgaruvchilarini yaratamiz.
# 3. son1  va son2 qiymatlarini qabul qilamiz.
# 4. son1 va son2 qo'shamiz, natijani summa ga yuklamymiz:
# 5. summa ni foydalanuvchiga qaytaramiz.
# 6. Stop

# Uchta sondan eng katta sonni topish Algoritmi:
# 1. Start
# 2. a, b, va c o'zgaruvchilarini yaratamiz
# 3. a, b va c ga qiymat beramiz
# 4. Agar a>b bo'lsa
#   agar a>c bo'lsa
#       a eng katta son deb qaytar
#      aks holda
#           c eng katta son deb qaytar 

# N faktorial den 1dan N gacha bo'lgan sonlarning ko'paytmasiga aytiladi.
# Masalan N=5 bo'lsa, 5 faktorial 1*2*3*4*5=120 ga teng.

