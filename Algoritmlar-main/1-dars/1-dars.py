# 3. faktorial va i ga 1 qiymatini yuklaymiz: 
# faktorial=1, i=1

# 4. Foydalanuvchidan n qiymatini qabul qilamiz.

# i=N bo'luniga qadar quyidagi qadamlarni takrorlaymiz.
